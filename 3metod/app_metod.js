        this.createdAt = new Date(); // –ó–∞–ø–∏—Å—ã–≤–∞–µ–º —Ç–µ–∫—É—â—É—é –¥–∞—Ç—É
        this.status = PaymentStatus.Holded; // –°—Ç–∞—Ç—É—Å –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é ‚Äî "–ó–∞–¥–µ—Ä–∂–∞–Ω"
